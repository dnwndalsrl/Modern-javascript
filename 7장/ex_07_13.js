// 지수 연산자
2 ** 2; // 4
2 ** 2.5; // 5.65685424...
2 ** 0; // 1
2 ** -2; // 0.25
2 ** (3 ** 2); // 512 

/*===================================*/
// Math.pow()
Math.pow(2, 2) // 4
Math.pow(2, 2.5); // 5.65685424...
Math.pow(2, 0); // 1
Math.pow(2, -2); // 0.25
Math.pow(2, Math.pow(3, 2)); // 512

/*===================================*/
// 음수를 거듭제곱의 밑으로 사용하려면 괄호로 묶어야한다
(-5) ** 2; // 25

/*===================================*/
// 다른 산술 연산자와 마찬가지로 할당 연산자와 함께 사용할 수 있다.
var num = 5;
num **= 2; // 25