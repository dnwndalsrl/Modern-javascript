// 할당 연산자
var x;

x = 10;
console.log(x); // 10

x += 5; // x = x + 5
console.log(x); // 15

x -= 5; // x = x - 5
console.log(x); // 10

x *= 5; // x = x * 5
console.log(x); // 50

x /= 5; // x = x / 5
console.log(x); // 10

x %= 5; // x = x % 5
console.log(x); // 0

// 문자열 연결 연산자
var str = 'My name is';

str += 'Lee'; // str = str + 'Lee';
console.log(str); // 'My name is Lee'