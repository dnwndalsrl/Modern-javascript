// typeof 연산자
// "string", "number", "boolean", "undefined", "symbol", "object", "function"
// 총 7가지의 문자열 중 하나를 반환한다.

typeof '' // "string"
typeof 1 // "number"
typeof NaN // "number"
typeof true // "boolean"
typeof undefined // "undefined"
typeof Symbol() // "symbol"
typeof null // "object"
typeof [] // "object"
typeof {} // "object"
typeof new Date() // "object"
typeof /test/gi // "object"
typeof function () {} // "function"

